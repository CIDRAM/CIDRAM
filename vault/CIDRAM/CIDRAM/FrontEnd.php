                if (
                    !($this->CIDRAM['FE']['hide-non-outdated'] && empty($this->CIDRAM['Components']['ThisComponent']['Outdated'])) &&
                    !($this->CIDRAM['FE']['hide-unused'] && empty($this->CIDRAM['Components']['ThisComponent']['Files']['To']))
