                        );
                    }
                    if ($this->CIDRAM['FE']['Previous'] === $this->CIDRAM['FE']['From']) {
                        $this->CIDRAM['FE']['Previous'] = '';
                    }
                    $this->CIDRAM['FE']['logfileData'] = $this->CIDRAM['FE']['logfileData'][1];
                    unset($this->CIDRAM['Iterations']);
                }

                /** Pagination counter. */
                $this->CIDRAM['FE']['Paginated'] = 1;

                /** Handle block filtering. */
                if (!empty($this->CIDRAM['FE']['logfileData']) && !empty($this->CIDRAM['QueryVars']['search'])) {
                    $this->CIDRAM['FE']['NewLogFileData'] = '';
                    $this->CIDRAM['FE']['Needle'] = 0;
                    $this->CIDRAM['FE']['BlockEnd'] = 0;
                    $this->CIDRAM['FE']['EntryCountPaginated'] = 0;
                    while ($this->stepThroughBlocks(
                        $this->CIDRAM['FE']['logfileData'],
                        $this->CIDRAM['FE']['Needle'],
                        $this->CIDRAM['FE']['BlockEnd'],
                        $this->CIDRAM['FE']['SearchQuery']
                    )) {
                        $this->CIDRAM['FE']['EntryCountBefore']++;
                        $this->CIDRAM['FE']['BlockStart'] = strrpos(substr($this->CIDRAM['FE']['logfileData'], 0, $this->CIDRAM['FE']['Needle']), $this->CIDRAM['FE']['BlockSeparator'], $this->CIDRAM['FE']['BlockEnd']);
                        $this->CIDRAM['FE']['BlockEnd'] = strpos($this->CIDRAM['FE']['logfileData'], $this->CIDRAM['FE']['BlockSeparator'], $this->CIDRAM['FE']['Needle']);
                        if ($this->CIDRAM['FE']['Paginate']) {
                            if (!$this->CIDRAM['FE']['From']) {
                                $this->CIDRAM['FE']['From'] = $this->isolateFirstFieldEntry(
                                    substr($this->CIDRAM['FE']['logfileData'], $this->CIDRAM['FE']['BlockStart'], $this->CIDRAM['FE']['BlockEnd'] - $this->CIDRAM['FE']['BlockStart']),
                                    $this->CIDRAM['FE']['FieldSeparator']
                                );
                            }
                            $this->CIDRAM['FE']['Paginated']++;
                            if ($this->CIDRAM['FE']['Paginated'] > ($this->CIDRAM['FE']['PerPage'] + 1)) {
                                if (!$this->CIDRAM['FE']['Next']) {
                                    $this->CIDRAM['FE']['Next'] = $this->isolateFirstFieldEntry(
                                        substr($this->CIDRAM['FE']['logfileData'], $this->CIDRAM['FE']['BlockStart'], $this->CIDRAM['FE']['BlockEnd'] - $this->CIDRAM['FE']['BlockStart']),
                                        $this->CIDRAM['FE']['FieldSeparator']
                                    );
                                }
                                continue;
                            }
                            $this->CIDRAM['FE']['EntryCountPaginated']++;
                            $this->CIDRAM['FE']['NewLogFileData'] .= substr($this->CIDRAM['FE']['logfileData'], $this->CIDRAM['FE']['BlockStart'], $this->CIDRAM['FE']['BlockEnd'] - $this->CIDRAM['FE']['BlockStart']);
                        } else {
                            $this->CIDRAM['FE']['NewLogFileData'] .= substr($this->CIDRAM['FE']['logfileData'], $this->CIDRAM['FE']['BlockStart'], $this->CIDRAM['FE']['BlockEnd'] - $this->CIDRAM['FE']['BlockStart']);
                        }
                    }
                    $this->CIDRAM['FE']['logfileData'] = rtrim($this->CIDRAM['FE']['NewLogFileData']) . $this->CIDRAM['FE']['BlockSeparator'];
                    unset($this->CIDRAM['FE']['Needle'], $this->CIDRAM['FE']['BlockSeparator'], $this->CIDRAM['FE']['BlockEnd'], $this->CIDRAM['FE']['BlockStart'], $this->CIDRAM['FE']['NewLogFileData']);
                    $this->CIDRAM['FE']['SearchInfoRender'] = (
                        $this->CIDRAM['FE']['Flags'] && preg_match('~^[A-Z]{2}$~', $this->CIDRAM['FE']['SearchQuery'])
                    ) ? '<span class="flag ' . $this->CIDRAM['FE']['SearchQuery'] . '"><span></span></span>' : '<code>' . $this->CIDRAM['FE']['SearchQuery'] . '</code>';
                    if ($this->CIDRAM['FE']['Paginate']) {
                        $this->CIDRAM['FE']['SearchInfo'] = '<br />' . sprintf(
                            $this->L10N->getPlural($this->CIDRAM['FE']['EntryCountBefore'], 'label_displaying_that_cite'),
                            '<span class="txtRd">' . $this->NumberFormatter->format($this->CIDRAM['FE']['EntryCountPaginated']) . '</span>' .
                            '<span class="txtBl">/</span>' .
                            '<span class="txtRd">' . $this->NumberFormatter->format($this->CIDRAM['FE']['EntryCountBefore']) . '</span>',
                            $this->CIDRAM['FE']['SearchInfoRender']
                        );
                        if ($this->CIDRAM['FE']['From']) {
                            $this->CIDRAM['FE']['SearchInfo'] .= '<br />' . sprintf(
                                $this->L10N->getString('label_starting_from'),
                                '<span class="txtRd">' . $this->CIDRAM['FE']['From'] . '</span>'
                            );
                            if ($this->CIDRAM['FE']['Previous']) {
                                $this->paginationFromLink('label_previous', $this->CIDRAM['FE']['Previous']);
                            }
                            if ($this->CIDRAM['FE']['Next']) {
                                $this->paginationFromLink('label_next', $this->CIDRAM['FE']['Next']);
                            }
                            if (isset($this->CIDRAM['FE']['EstAft'])) {
                                $this->CIDRAM['FE']['EstAft'] = floor(($this->CIDRAM['FE']['EstAft'] / (($this->CIDRAM['FE']['EstAft'] + $this->CIDRAM['FE']['EstFore']) ?: 1)) * 100);
                                if ($this->CIDRAM['FE']['EstFore'] <= $this->CIDRAM['FE']['PerPage']) {
                                    $this->CIDRAM['FE']['EstWidth'] = 100 - $this->CIDRAM['FE']['EstAft'];
                                } else {
                                    $this->CIDRAM['FE']['EstWidth'] = floor(($this->CIDRAM['FE']['EntryCountPaginated'] / ($this->CIDRAM['FE']['EntryCountBefore'] ?: $this->CIDRAM['FE']['EntryCount'])) * 100);
                                }
                                $this->CIDRAM['FE']['SearchInfo'] .= sprintf(
                                    '<br /><div style="width:100%%;height:2px;overflow:visible;background-color:rgba(0,192,0,.4);margin:1px 0 1px 0">' .
                                    '<div style="position:relative;%s:%d%%;top:-1px;width:%d%%;height:4px;overflow:visible;background-color:rgba(192,0,0,.5);margin:0"></div></div>',
                                    $this->CIDRAM['FE']['FE_Align'],
                                    $this->CIDRAM['FE']['EstAft'],
                                    $this->CIDRAM['FE']['EstWidth']
                                );
                            }
                        }
                    } else {
                        $this->CIDRAM['FE']['SearchInfo'] = '<br />' . sprintf(
                            $this->L10N->getPlural($this->CIDRAM['FE']['EntryCountBefore'], 'label_displaying_that_cite'),
                            '<span class="txtRd">' . $this->NumberFormatter->format($this->CIDRAM['FE']['EntryCountBefore']) . '</span>',
                            $this->CIDRAM['FE']['SearchInfoRender']
                        );
                    }
                } else {
                    if ($this->CIDRAM['FE']['Paginate']) {
                        $this->CIDRAM['FE']['NewLogFileData'] = '';
                        $this->CIDRAM['FE']['OriginalLogDataLen'] = strlen($this->CIDRAM['FE']['logfileData']);
                        $this->CIDRAM['FE']['BlockStart'] = 0;
                        $this->CIDRAM['FE']['BlockEnd'] = 0;
                        if (!$this->CIDRAM['FE']['From']) {
                            $this->CIDRAM['FE']['From'] = $this->isolateFirstFieldEntry(
                                $this->CIDRAM['FE']['logfileData'],
                                $this->CIDRAM['FE']['FieldSeparator']
                            );
                        }
                        while (true) {
                            $this->CIDRAM['FE']['BlockOffset'] = $this->CIDRAM['FE']['BlockStart'] + $this->CIDRAM['FE']['BlockSepLen'];
                            if ($this->CIDRAM['FE']['BlockOffset'] >= $this->CIDRAM['FE']['OriginalLogDataLen']) {
                                break;
                            }
                            $this->CIDRAM['FE']['BlockEnd'] = strpos($this->CIDRAM['FE']['logfileData'], $this->CIDRAM['FE']['BlockSeparator'], $this->CIDRAM['FE']['BlockStart']);
                            if ($this->CIDRAM['FE']['BlockEnd'] === false) {
                                break;
                            }
                            $this->CIDRAM['FE']['NewLogFileData'] .= substr($this->CIDRAM['FE']['logfileData'], $this->CIDRAM['FE']['BlockStart'], ($this->CIDRAM['FE']['BlockEnd'] - $this->CIDRAM['FE']['BlockStart']) + $this->CIDRAM['FE']['BlockSepLen']);
                            $this->CIDRAM['FE']['Paginated']++;
                            if ($this->CIDRAM['FE']['Paginated'] > $this->CIDRAM['FE']['PerPage']) {
                                if (!$this->CIDRAM['FE']['Next']) {
                                    $this->CIDRAM['FE']['Next'] = $this->isolateFirstFieldEntry(
                                        substr($this->CIDRAM['FE']['logfileData'], $this->CIDRAM['FE']['BlockEnd'] + $this->CIDRAM['FE']['BlockSepLen']),
                                        $this->CIDRAM['FE']['FieldSeparator']
                                    );
                                }
                                break;
                            }
                            $this->CIDRAM['FE']['BlockStart'] = $this->CIDRAM['FE']['BlockEnd'] + $this->CIDRAM['FE']['BlockSepLen'];
                        }
                        $this->CIDRAM['FE']['logfileData'] = $this->CIDRAM['FE']['NewLogFileData'];
                        unset(
                            $this->CIDRAM['FE']['BlockOffset'],
                            $this->CIDRAM['FE']['BlockSepLen'],
                            $this->CIDRAM['FE']['BlockSeparator'],
                            $this->CIDRAM['FE']['BlockEnd'],
                            $this->CIDRAM['FE']['BlockStart'],
                            $this->CIDRAM['FE']['OriginalLogDataLen'],
                            $this->CIDRAM['FE']['NewLogFileData']
                        );
                    }
                    $this->CIDRAM['FE']['EntryCount'] = !str_replace("\n", '', $this->CIDRAM['FE']['logfileData']) ? 0 : (
                        substr_count($this->CIDRAM['FE']['logfileData'], "\n\n") ?: substr_count($this->CIDRAM['FE']['logfileData'], "\n")
                    );
                    if ($this->CIDRAM['FE']['Paginate']) {
                        $this->CIDRAM['FE']['SearchInfo'] = '<br />' . sprintf(
                            $this->L10N->getPlural($this->CIDRAM['FE']['EntryCountBefore'], 'label_displaying'),
                            '<span class="txtRd">' . $this->NumberFormatter->format($this->CIDRAM['FE']['EntryCount']) . '</span>' .
                            '<span class="txtBl">/</span>' .
                            '<span class="txtRd">' . $this->NumberFormatter->format($this->CIDRAM['FE']['EntryCountBefore']) . '</span>'
                        );
                        if ($this->CIDRAM['FE']['From']) {
                            $this->CIDRAM['FE']['SearchInfo'] .= '<br />' . sprintf(
                                $this->L10N->getString('label_starting_from'),
                                '<span class="txtRd">' . $this->CIDRAM['FE']['From'] . '</span>'
                            );
                            if ($this->CIDRAM['FE']['Previous']) {
                                $this->paginationFromLink('label_previous', $this->CIDRAM['FE']['Previous']);
                            }
                            if ($this->CIDRAM['FE']['Next']) {
                                $this->paginationFromLink('label_next', $this->CIDRAM['FE']['Next']);
                            }
                            if (isset($this->CIDRAM['FE']['EstAft'])) {
                                $this->CIDRAM['FE']['EstAft'] = floor(($this->CIDRAM['FE']['EstAft'] / (($this->CIDRAM['FE']['EstAft'] + $this->CIDRAM['FE']['EstFore']) ?: 1)) * 100);
                                if ($this->CIDRAM['FE']['EstFore'] <= $this->CIDRAM['FE']['PerPage']) {
                                    $this->CIDRAM['FE']['EstWidth'] = 100 - $this->CIDRAM['FE']['EstAft'];
                                } else {
                                    $this->CIDRAM['FE']['EstWidth'] = floor(($this->CIDRAM['FE']['EntryCount'] / ($this->CIDRAM['FE']['EntryCountBefore'] ?: $this->CIDRAM['FE']['EntryCount'])) * 100);
                                }
                                $this->CIDRAM['FE']['SearchInfo'] .= sprintf(
                                    '<br /><div style="width:100%%;height:2px;overflow:visible;background-color:rgba(0,192,0,.4);margin:1px 0 1px 0">' .
                                    '<div style="position:relative;%s:%d%%;top:-1px;width:%d%%;height:4px;overflow:visible;background-color:rgba(192,0,0,.5);margin:0"></div></div>',
                                    $this->CIDRAM['FE']['FE_Align'],
                                    $this->CIDRAM['FE']['EstAft'],
                                    $this->CIDRAM['FE']['EstWidth']
                                );
                            }
                        }
                    } else {
                        $this->CIDRAM['FE']['SearchInfo'] = '<br />' . sprintf(
                            $this->L10N->getPlural($this->CIDRAM['FE']['EntryCount'], 'label_displaying'),
                            '<span class="txtRd">' . $this->NumberFormatter->format($this->CIDRAM['FE']['EntryCount']) . '</span>'
                        );
                    }
                }

                $this->CIDRAM['FE']['logfileData'] = $this->CIDRAM['FE']['TextModeLinks'] === 'fancy' ? str_replace(
                    ['<', '>', "\r", "\n"],
                    ['&lt;', '&gt;', '', "<br />\n"],
                    $this->CIDRAM['FE']['logfileData']
                ) : str_replace(
                    ['<', '>', "\r"],
                    ['&lt;', '&gt;', ''],
                    $this->CIDRAM['FE']['logfileData']
                );
                $this->CIDRAM['FE']['mod_class_nav'] = ' big';
                $this->CIDRAM['FE']['mod_class_right'] = ' extend';
            }
            if (empty($this->CIDRAM['FE']['mod_class_nav'])) {
                $this->CIDRAM['FE']['mod_class_nav'] = ' extend';
                $this->CIDRAM['FE']['mod_class_right'] = ' big';
            }

            /** Logs control form. */
            $this->CIDRAM['FE']['TextModeSwitchLink'] = sprintf(
                '<td class="h4"><span class="s">%1$s<br /><select name="textMode" class="auto">' .
                '<option value="simple"%2$s>%3$s</option>' .
                '<option value="fancy"%4$s>%5$s</option>' .
                '<option value="tally"%6$s>%7$s</option>' .
                '</select></span></td><td class="h4f"><span class="s">' .
                '<input type="radio" class="auto" name="sortOrder" value="ascending" id="sOa"%8$s /><label for="sOa">%9$s</label><br />' .
                '<input type="radio" class="auto" name="sortOrder" value="descending" id="sOd"%10$s /><label for="sOd">%11$s</label>' .
                '</span></td></tr><tr><td class="h4"><span class="s">' .
                '<input type="checkbox" name="paginate" class="auto" id="paginate"%16$s /><label for="paginate">%17$s</label><br />' .
                '<label for="perpage">%18$s</label><br /><input type="number" name="perpage" class="auto" id="perpage" value="%19$d" />' .
                '</span></td><td class="h4f"><span class="s">' .
                '<input type="checkbox" name="remember" class="auto" id="remember"%12$s /><label for="remember">%13$s</label><br />' .
                '<input type="hidden" name="logfile" value="%14$s" /><input type="submit" value="%15$s" />' .
                '</span></td>',
                $this->L10N->getString('label_textmode'),
                $this->CIDRAM['FE']['TextModeLinks'] === 'simple' ? ' selected' : '',
                $this->L10N->getString('label_textmode_simple'),
                $this->CIDRAM['FE']['TextModeLinks'] === 'fancy' ? ' selected' : '',
                $this->L10N->getString('label_textmode_fancy'),
                $this->CIDRAM['FE']['TextModeLinks'] === 'tally' ? ' selected' : '',
                $this->L10N->getString('label_textmode_tally'),
                $this->CIDRAM['FE']['SortOrder'] === 'ascending' ? ' checked' : '',
                $this->L10N->getString('switch-descending-order-set-false'),
                $this->CIDRAM['FE']['SortOrder'] === 'descending' ? ' checked' : '',
                $this->L10N->getString('switch-descending-order-set-true'),
                $this->CIDRAM['FE']['Remember'] ? ' checked' : '',
                $this->L10N->getString('label_remember'),
                $this->CIDRAM['QueryVars']['logfile'] ?? '',
                $this->L10N->getString('field_ok'),
                $this->CIDRAM['FE']['Paginate'] ? ' checked' : '',
                $this->L10N->getString('label_paginate'),
                $this->L10N->getString('label_entries_per_page'),
                $this->CIDRAM['FE']['PerPage']
            );

            /** Prepare log data formatting. */
            if ($this->CIDRAM['FE']['TextModeLinks'] === 'fancy') {
                $this->formatter($this->CIDRAM['FE']['logfileData'], $this->CIDRAM['FE']['BlockLink'], $this->CIDRAM['FE']['SearchQuery'], $this->CIDRAM['FE']['FieldSeparator'], $this->CIDRAM['FE']['Flags']);
            } elseif ($this->CIDRAM['FE']['TextModeLinks'] === 'tally') {
                $this->CIDRAM['FE']['logfileData'] = $this->tally(
                    $this->CIDRAM['FE']['logfileData'],
                    $this->CIDRAM['FE']['BlockLink'],
                    [$this->L10N->getString('field_id'), $this->L10N->getString('field_datetime')]
                );
            } else {
                $this->CIDRAM['FE']['logfileData'] = '<textarea readonly>' . trim($this->CIDRAM['FE']['logfileData']) . '</textarea>';
            }

            /** Generate a list of the logs. */
            foreach ($this->CIDRAM['FE']['LogFiles']['Files'] as $this->CIDRAM['FE']['LogFiles']['ThisLogFile']) {
                $this->CIDRAM['FE']['LogFiles']['Out'] .= sprintf(
                    '      <a href="?cidram-page=logs&textMode=%1$s&sortOrder=%2$s%3$s&logfile=%4$s">%4$s</a> – %5$s<br />',
                    $this->CIDRAM['FE']['TextModeLinks'],
                    $this->CIDRAM['FE']['SortOrder'],
                    $this->CIDRAM['FE']['Remember'] ? '&remember=on' : '',
                    $this->CIDRAM['FE']['LogFiles']['ThisLogFile']['Filename'],
                    $this->CIDRAM['FE']['LogFiles']['ThisLogFile']['Filesize']
                ) . "\n";
            }

            /** Calculate page load time (useful for debugging). */
            $this->CIDRAM['FE']['ProcessTime'] = microtime(true) - $_SERVER['REQUEST_TIME_FLOAT'];
            $this->CIDRAM['FE']['SearchInfo'] = '<td colspan="3" class="spanner">' . sprintf(
                $this->L10N->getPlural($this->CIDRAM['FE']['ProcessTime'], 'state_loadtime'),
                '<span class="txtRd">' . $this->NumberFormatter->format($this->CIDRAM['FE']['ProcessTime'], 3) . '</span>'
            ) . $this->CIDRAM['FE']['SearchInfo'] . '</td>';

            /** Set logfile list or no logfiles available message. */
            $this->CIDRAM['FE']['LogFiles'] = $this->CIDRAM['FE']['LogFiles']['Out'] ?: $this->L10N->getString('logs_no_logfiles_available');

            /** Send output. */
            echo $this->sendOutput();
        }

        /** Destroy cache object and some related values. */
        $this->destroyCacheObject();

        /** Print Cronable failure state messages here. */
        if ($this->CIDRAM['FE']['CronMode'] !== '' && $this->CIDRAM['FE']['state_msg'] !== '' && $this->CIDRAM['FE']['UserState'] !== 1) {
            if ($this->CIDRAM['FE']['CronType'] === 'localUpdate') {
                $Results = ['state_msg' => $this->CIDRAM['FE']['state_msg']];
            } else {
                echo json_encode(['state_msg' => $this->CIDRAM['FE']['state_msg']]);
            }
        }

        /** Exit front-end. */
        if (empty($this->Alternate) && $this->CIDRAM['FE']['CronType'] !== 'localUpdate') {
            die;
        }
    }
}
