                                    $Previous = $Param;
                                }
                                if ($Param !== $Previous) {
                                    $NEoLPos = 0;
                                    break;
                                }
                                if ($Line) {
                                    $Set .= $Line . "\n";
                                }
                                if ($NEoLPos === false) {
                                    break;
                                }
                                $EoLPos = $NEoLPos;
                            }
