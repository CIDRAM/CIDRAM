                    empty($this->CIDRAM['Components']['ThisComponent']['Files']['To']) ||
                    count($this->CIDRAM['Components']['ThisComponent']['Files']['To']) !== 1
                ) ? '' : '<br />' . $this->L10N->getString('field_filename') . ' ' . $this->CIDRAM['Components']['ThisComponent']['Files']['To'][0];

